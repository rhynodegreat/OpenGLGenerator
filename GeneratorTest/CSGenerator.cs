        string ToCamelCase(string input) {
            var builder = new StringBuilder();
            string[] tokens = input.Split('_');
            for (int i = 1; i < tokens.Length; i++) {
                string token = tokens[i];
                bool first = false;
                if (token.Contains("RG")) { //RG RGB RGBA SRGB
                    builder.Append(token);
                    continue;
                }
                for (int j = 0; j < token.Length; j++) {
                    char c = token[j];
                    if (char.IsDigit(c)) {
                        if (i == 1 && j == 0) {
                            builder.Append('_');
                        }
                        builder.Append(c);
                    } else if (char.IsLetter(c)) {
                        if (!first) {
                            builder.Append(char.ToUpper(c));
                            first = true;
                        } else {
                            builder.Append(char.ToLower(c));
                        }
                    }
                }
            }

            return builder.ToString();
        }

        string GetName(string input) {
            switch (input) {
                case "ref":
                    return "_ref";
                case "params":
                    return "_params";
                case "string":
                    return "_string";
                case "base":
                    return "_base";
                default:
                    return input;
            }
        }

        string GetReturnType(string input, CSCommand command) {
            switch (input) {
                case "GLenum":
                    if (command.ReturnGroup != null) {
                        return command.ReturnGroup;
                    } else {
                        return "uint";
                    }
                case "GLsync":
                    return "IntPtr";
                case "GLfloat":
                    return "float";
                case "GLboolean":
                    return "bool";
                case "GLubyte":
                    return "byte";
                case "GLushort":
                    return "ushort";
                case "GLshort":
                    return "short";
                case "GLint":
                    return "int";
                case "GLuint":
                    return "uint";
                case "GLuint64":
                    return "long";
                case "GLbitfield":
                    return "uint";
                default:
                    return input;
            }
        }

